<h2>Cross Platform</h2>
<p>Lazarus runs on Windows, MacOS, Linux and many other platforms. Even on Raspberry Pi! The programs you create also runs on these platforms. Enjoy the same experience on your favorite OS. See <a href="http://wiki.freepascal.org/Installing_Lazarus">Installing Lazarus</a> for OS-specific instructions for installation.</p>

<h2>Powerful, Fast</h2>
<p>Lazarus is a capable IDE for handling large projects. Its compiler FPC is being constantly developed to improve performance. As an example for the project size the IDE can handle, and the performance of the resulting application: The Lazarus IDE itself is developed using Lazarus.</p>

<h2>Drag and Drop Form Designer</h2>
<p>Lazarus has a graphical form designer with guidelines for aligning with adjacent components. There are numerous components for almost every software imaginable, already ready to be used. Further components can be added to the IDE by Lazarus Package Files (LPKs).</p>

<h2>Cross-platform</h2>
<p>Lazarus form designer uses LCL (Lazarus Component Library) which is especially designed for cross platform usage. Building your project for different platforms will give you native look and feel on each platform. There are no changes to the project needed.</p>

<h2>Modern, Object oriented language</h2>
<p>Lazarus uses Free Pascal as its language which is an Object Pascal dialect. It is constantly developed to integrate new features that can be expected in modern programming  languages.</p>

<h2>Easily Extendable</h2>
<p>Hundreds of developers are developing and maintaining their Lazarus Libraries and Lazarus Packages (LPKs). You can find almost any kind of library that you may need.</p>

<h2>Open source</h2>
<p>Lazarus is open source and the core libraries <sup>[4]</sup> are distributed under LGPL with the extra permission  of static linking. So you can create non-commercial and commercial applications with it.</p>
<p><sup>[4]</sup> This applies to LCL, FCL, RTL. It covers all the standard components and many other. Some of the packages with extra components may be GPL, MPL or other</p>

<h2>Frameworks</h2>
<p>Various Frameworks are available which saves your time on certain codebase.</p>

