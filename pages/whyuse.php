<p>Lazarus is a versatile IDE with many features. There are many reasons why Lazarus could be chosen over other IDEs:</p>

<h2>Lazarus is Cross platform</h2>

<p>There are many IDEs available for free and commercial as well. But a very few can support the platforms supported by Lazarus. Lazarus can be run in Windows, Mac OS X, Linux, BSD, Raspberry Pi etc. It gives similar programming experience in all of the platforms.</p>

<h2>Get started right away</h2>

<p>Just installing Lazarus gives you enough to get you started. When installed, Lazarus is ready to create anything from console to graphical applications. Lazarus has a built-in drag and drop form designer which cleverly integrates with your code. You also get Free Pascal Manuals, example codes, debugger, fpDoc documentation utility and much more with the default installation.</p>

<h2>No dependencies!</h2>

<p>With Lazarus you can create programs which do not require any platform dependencies <sup>[1]</sup>. The result of it is the user of your program does not need to install any further packages, libraries or frameworks to run your software.</p>

<p><sup>[1]</sup> Linux/BSD applications may depend on GTK2 or alternatively QT. Some add-on packages may also add dependencies of their own<br /></p>

<h2>Easier Language</h2>

<p>Lazarus is made for writing object orientated Pascal code. Lazarus is the IDE for Free Pascal. Free Pascal is an Object Pascal derivative of Pascal. Pascal was designed for educating computer science students to learn programming easily. The syntax which is well formed and easier to understand can benefit you greatly in your programming.</p>

<h2>Can be used in commercial projects</h2>

<p>Some IDEs restrict their license to only non-commercial development. Lazarus is GPL/LGPL <sup>[2]</sup><sup>[3]</sup> which permits using it in building commercial projects.</p>
<p>
	<sup>[2]</sup> LGPL with additional permission to link libraries into your binaries.<br />
	<sup>[3]</sup> Some additional packages come with various licenses such as GPL, MPL, &hellip;<br />
</p>

